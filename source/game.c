void LoadSRAMData()
{
    savegame_read(&game_settings, sizeof(GameSettings));
    if( game_settings.game_save_marker != 0xDEADBEEF )
    {
        game_settings.game_save_marker = 0xDEADBEEF;
        game_settings.game_tutorials_enabled = 1;
        game_settings.game_current_level = 0;
        game_settings.game_maximum_level = 0;
        game_settings.game_practice_mode = 0;
        game_settings.game_current_level_moves_remaining = 0;
        for(int i = 0; i < 80; i++)
        {
            game_settings.game_current_level_map_layout[i] = INVALID_MAP;
        }
        SaveSRAMData();
    }
}

void SaveSRAMData()
{
    savegame_write(&game_settings, sizeof(GameSettings));
}


