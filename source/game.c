void game_dialoguebox(u8 Plane, u8 X, u8 Y, u8 Width, u8 Height)
{
    u16 usetile = 0;
    graphics_tilemap_tilemap_settile(Plane, X, Y, 264);
    graphics_tilemap_tilemap_settile(Plane, X + Width - 1, Y, 265);
    graphics_tilemap_tilemap_settile(Plane, X, Y + Height - 1, 266);
    graphics_tilemap_tilemap_settile(Plane, X + Width - 1, Y + Height - 1, 267);
    for(u8 y = Y + 1; y < Y + Height - 1; y++ )
    {
        graphics_tilemap_tilemap_settile(Plane, X, y, 268);
        graphics_tilemap_tilemap_settile(Plane, X + Width - 1, y, 271);
        for(u8 x = X + 1; x < X + Width - 1; x++ )
        {
            usetile = 254;
            graphics_tilemap_tilemap_settile(Plane, x, y, usetile);
        }
    }
    for(u8 x = X + 1; x < X + Width - 1; x++ )
    {
        graphics_tilemap_tilemap_settile(Plane, x, Y, 269);
        graphics_tilemap_tilemap_settile(Plane, x, Y + Height - 1, 270);
    }
}

u8 game_dialoguebox_sprited(u8 Plane, u8 X, u8 Y, const char* text, u16 text_len, u8 SpriteStartIndex)
{
    u16 spriteindex = SpriteStartIndex;
    u8 xl = X;
    u8 xr = X + text_len + 1; // X + text_len - 1;
    u16 tile;

    for (u16 x = xl; x <= xr; x++)
    {

        tile = 269 << 1;
        if (x == xl) { tile = 264 << 1; }
        if (x == xr) { tile = 265 << 1; }
        graphics_sprites_configure(spriteindex, tile, COLOURS_8BPP, 0, SIZE_8x8);
        graphics_sprites_position(spriteindex, Plane, 1, x << 3, Y << 3, MIRROR_NONE);
        spriteindex++;

        if (x > xl)
        {
            graphics_sprites_configure(spriteindex, text[x - xl - 1] << 1, COLOURS_8BPP, 0, SIZE_8x8);
            graphics_sprites_position(spriteindex, Plane, 1, x << 3, (Y + 1) << 3, MIRROR_NONE);
            spriteindex++;
        }

        tile = 254 << 1;
        if (x == xl) { tile = 268 << 1; }
        if (x == xr) { tile = 271 << 1; }
        graphics_sprites_configure(spriteindex, tile, COLOURS_8BPP, 0, SIZE_8x8);
        graphics_sprites_position(spriteindex, Plane, 1, x << 3, (Y + 1) << 3, MIRROR_NONE);
        spriteindex++;

        tile = 270 << 1;
        if (x == xl) { tile = 266 << 1; }
        if (x == xr) { tile = 267 << 1; }
        graphics_sprites_configure(spriteindex, tile, COLOURS_8BPP, 0, SIZE_8x8);
        graphics_sprites_position(spriteindex, Plane, 1, x << 3, (Y + 2) << 3, MIRROR_NONE);
        spriteindex++;

    }
    return spriteindex - SpriteStartIndex;
}

void game_print(u8 Plane, u8 X, u8 Y, const char* text, u16 text_len)
{
	for(int p = 0; p < text_len; p++)
	{
		graphics_tilemap_tilemap_settile(Plane, X + p, Y, (u16)text[p]);
	}
}

void game_printlargenumber(u8 Plane, u8 X, u8 Y, u16 Number, u8 DrawWidth)
{
    // Blank = 96
    // 0 = 97
    // 9 = 106
    u8 curx = X + DrawWidth - 1;
    u16 curnum = Number;
    if(Number == 0)
    {
        graphics_tilemap_tilemap_settile(Plane, curx, Y, 97);
        graphics_tilemap_tilemap_settile(Plane, curx, Y + 1, 113);
        return;
    }
    while(curx >= X)
    {
        if(curnum == 0)
        {
            graphics_tilemap_tilemap_settile(Plane, curx, Y, 96);
            graphics_tilemap_tilemap_settile(Plane, curx, Y + 1, 96);
        }
        else
        {
            u8 lowestdigit = (curnum % 10);
            graphics_tilemap_tilemap_settile(Plane, curx, Y, 97 + lowestdigit);
            graphics_tilemap_tilemap_settile(Plane, curx, Y + 1, 113 + lowestdigit);
            curnum = (curnum - lowestdigit) / 10;
        }
        curx--;
    }

}

